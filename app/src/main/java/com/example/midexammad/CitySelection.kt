@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun CitySelection(navController: NavController) {


    Scaffold(
        topBar = {
            TopAppBar(
                colors = TopAppBarDefaults.topAppBarColors(
                    containerColor = MaterialTheme.colorScheme.primaryContainer,
                    titleContentColor = MaterialTheme.colorScheme.primary,
                ),
                title = {
                    Text("Select City")
                }
            )
        },
        bottomBar = {
            BottomAppBar(
                containerColor = MaterialTheme.colorScheme.primaryContainer,
                contentColor = MaterialTheme.colorScheme.primary,
            ) {
                Text(
                    modifier = Modifier
                        .fillMaxWidth(),
                    textAlign = TextAlign.Center,
                    text = "~",
                )
            }
        },
    ) { innerPadding ->
        val cityInput = remember {
            mutableStateOf("")
        }
        Column(


            modifier = Modifier
                .padding(innerPadding)
                .fillMaxSize(),
            verticalArrangement = Arrangement.spacedBy(16.dp),
            horizontalAlignment = Alignment.CenterHorizontally
        ) {
            Image(painter = painterResource(id = R.drawable.logo), contentDescription ="Logo" )
            Image(painter = painterResource(id = R.drawable.map), contentDescription ="Map" )

            TextField(
                value = cityInput.value,
                onValueChange = { cityInput.value =it},
             placeholder = "Enter City Name")

            Button(onClick = {
                navController.navigate("WeatherDetails"+"/{$cityInput}")
            }) {
                Text("Show Weather")
            }


        }

    }

}